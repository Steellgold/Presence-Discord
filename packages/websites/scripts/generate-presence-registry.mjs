import { mkdir, readdir, stat, writeFile } from "node:fs/promises";
import { dirname, join, relative, sep } from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = dirname(fileURLToPath(new URL("../package.json", import.meta.url)));
const srcRoot = join(packageRoot, "src");
const generatedRoot = join(srcRoot, ".generated");
const registryPath = join(generatedRoot, "presences.ts");
const buckets = ["#", ..."ABCDEFGHIJKLMNOPQRSTUVWXYZ"];

const toImportName = (parts) =>
  `presence${parts
    .join("_")
    .replaceAll(/[^a-zA-Z0-9_]/g, "_")
    .replaceAll(/_+/g, "_")}`;

const pathExists = async (path) => {
  try {
    await stat(path);
    return true;
  } catch {
    return false;
  }
};

const collectPresenceFiles = async () => {
  const presenceFiles = [];

  for (const bucket of buckets) {
    const bucketPath = join(srcRoot, bucket);

    if (!(await pathExists(bucketPath))) {
      continue;
    }

    const entries = await readdir(bucketPath, { withFileTypes: true });

    for (const entry of entries) {
      if (!entry.isDirectory()) {
        continue;
      }

      const presencePath = join(bucketPath, entry.name, "presence.ts");
      const metadataPath = join(bucketPath, entry.name, "metadata.json");

      if ((await pathExists(presencePath)) && (await pathExists(metadataPath))) {
        presenceFiles.push(presencePath);
      }
    }
  }

  return presenceFiles.sort((left, right) => left.localeCompare(right));
};

const toModulePath = (path) => {
  const relativePath = relative(generatedRoot, path).split(sep).join("/");
  const jsPath = relativePath.replace(/\.ts$/, ".js");

  return jsPath.startsWith(".") ? jsPath : `./${jsPath}`;
};

const presenceFiles = await collectPresenceFiles();
const imports = presenceFiles.map((file) => {
  const parts = relative(srcRoot, file).split(sep).slice(0, -1);

  return {
    name: toImportName(parts),
    path: toModulePath(file),
  };
});

const contents = [
  "/* This file is generated by scripts/generate-presence-registry.mjs. */",
  'import type { PresenceDefinition } from "../types.js";',
  "",
  ...imports.map(({ name, path }) => `import ${name} from "${path}";`),
  "",
  "export const presenceRegistry = [",
  ...imports.map(({ name }) => `  ${name},`),
  "] as const satisfies readonly PresenceDefinition[];",
  "",
].join("\n");

await mkdir(generatedRoot, { recursive: true });
await writeFile(registryPath, contents, "utf8");
